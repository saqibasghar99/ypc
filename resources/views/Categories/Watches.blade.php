<script>
    $(document).ready(function() {
        // Simple add to cart animation
        $('.add-to-cart-button').click(function() {
            const currentCount = parseInt($('.cart-count').text());
            $('.cart-count').text(currentCount + 1);
            
            // Animation
            $(this).find('.material-icons').addClass('animate__animated animate__bounce');
            setTimeout(() => {
                $(this).find('.material-icons').removeClass('animate__animated animate__bounce');
            }, 1000);
            
            // Show notification
            Swal.fire({
                position: 'top-end',
                icon: 'success',
                title: 'Added to cart!',
                showConfirmButton: false,
                timer: 1500
            });
        });
    });
